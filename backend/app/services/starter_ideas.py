def generate_starter_ideas(count: int = 3, seed: int | None = None) -> list[str]:
    """
    Generate a set of mediocre starter ideas for a new session.

    Uses McDonald's theory: people are more willing to critique and improve
    mediocre ideas than to create ideas from scratch on a blank slate.

    Args:
        count: Number of starter ideas to generate (default: 3, max: 10)
        seed: Random seed for reproducibility (optional)

    Returns:
        List of starter idea strings

    Examples:
        >>> ideas = generate_starter_ideas(3)
        >>> len(ideas)
        3
        >>> ideas = generate_starter_ideas(5, seed=42)
        >>> len(ideas)
        5
    """
    if seed is not None:
        random.seed(seed)

    # Limit to available templates and max of 10
    actual_count = min(count, len(STARTER_IDEA_TEMPLATES), 10)

    # Sample without replacement
    selected_ideas = random.sample(STARTER_IDEA_TEMPLATES, actual_count)

    return selected_ideas
